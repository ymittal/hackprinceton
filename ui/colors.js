export function black (alpha = 1) {
  return `rgba(0, 0, 0, ${alpha})`
}

export function blue (alpha = 1) {
  return `rgba(110, 149, 172, ${alpha})`
}

export function lightBlue (alpha = 1) {
  return `rgba(110, 225, 255, ${alpha})`
}

export function orange (alpha = 1) {
  return `rgba(255, 175, 60, ${alpha})`
}

export function gradientBottom (alpha = 1) {
  return `rgba(78, 161, 241, ${alpha})`
}

export function gradientTop (alpha = 1) {
  return `rgba(140, 212, 249, ${alpha})`
}
